    private void btnRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunActionPerformed
        
        String targetName = "scripts";
        if(radioScripts.isSelected())
            targetName = "scripts";
        else if(radioDeploy.isSelected())
            targetName = "deploy";
        else if(radioGetTemplates.isSelected())
            targetName = "get-templates";
        try 
        {
            File cwd = new File(System.getProperty("user.dir"));
            Runtime.getRuntime().exec("cmd /c start ant.bat -f ant/build.xml " + targetName, null, cwd);
        } catch (IOException ex) 
        {
                JOptionPane.showMessageDialog(null, "Error launching external command window" +
                        ex.getMessage(), "Ant error", JOptionPane.ERROR_MESSAGE);
        }
        
        setVisible(false);
        dispose();
    }//GEN-LAST:event_btnRunActionPerformed

